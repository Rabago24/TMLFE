function getSalaryAmount(salary) {
    if (salary === null || salary === undefined || salary === "" || salary === "-") return 0;
    if (typeof salary === "number") return Number.isFinite(salary) ? salary : 0;
    const match = String(salary).match(/[\d.]+/);
    return match ? Number(match[0].replace(/\./g, "")) || 0 : 0;
}

function getSalaryOption(salary) {
    if (typeof salary !== "string") return "";
    if (/\bTO\b/i.test(salary)) return "TO";
    if (/\bPO\b/i.test(salary)) return "PO";
    return "";
}

function getTeamById(id) {
    return TMLFE.teams.find(team => team.id === Number(id));
}

function getTeamByName(name) {
    const query = String(name || "").trim().toLowerCase();
    return TMLFE.teams.find(team =>
        team.name.toLowerCase() === query || team.short.toLowerCase() === query
    );
}

function getPlayersByTeam(teamName) {
    const team = getTeamByName(teamName);
    const resolvedName = team ? team.name : String(teamName || "");
    return TMLFE.players.filter(player => player.team === resolvedName);
}

function getPlayerByName(playerName) {
    const query = String(playerName || "").trim().toLowerCase();
    return TMLFE.players.find(player => player.name.toLowerCase() === query);
}

function getTeamPayroll(teamName, season = TMLFE.season) {
    return getPlayersByTeam(teamName).reduce(
        (total, player) => total + getSalaryAmount(player.salaries?.[season]),
        0
    );
}

function getTeamCapSpace(teamName, season = TMLFE.season) {
    return TMLFE.salaryCap - getTeamPayroll(teamName, season);
}

function getTeamSummary(teamName, season = TMLFE.season) {
    const team = getTeamByName(teamName);
    if (!team) return null;
    const players = getPlayersByTeam(team.name);
    const payroll = getTeamPayroll(team.name, season);
    const averageRating = players.length
        ? players.reduce((sum, player) => sum + player.rating, 0) / players.length
        : 0;
    const averageAge = players.length
        ? players.reduce((sum, player) => sum + player.age, 0) / players.length
        : 0;

    return {
        ...team,
        rosterSize: players.length,
        payroll,
        capSpace: TMLFE.salaryCap - payroll,
        averageRating: Number(averageRating.toFixed(1)),
        averageAge: Number(averageAge.toFixed(1))
    };
}

